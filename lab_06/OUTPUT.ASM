EXTRN TO_UNSIGNED_HEX : NEAR
EXTRN TO_SIGNED_BIN : NEAR
EXTRN HEX_STR : BYTE
EXTRN BIN_STR : BYTE
EXTRN SIGN : BYTE

PUBLIC MOVE_CARRIAGE
PUBLIC OUTPUT_UNSIGNED_HEX
PUBLIC OUTPUT_SIGNED_BIN

DATASEG SEGMENT PARA PUBLIC 'DATA'
    HWX_MSG DB 'Unsigned hexadecimal number: $'
    BIN_MSG DB 'Signed binary number: $'
DATASEG ENDS

CODESEG SEGMENT PARA PUBLIC 'CODE'
    ASSUME CS:CODESEG

MOVE_CARRIAGE PROC NEAR
    MOV AH, 2
    MOV DL, 13
    INT 21H
    MOV DL, 10
    INT 21H
    RET
MOVE_CARRIAGE ENDP

OUTPUT_UNSIGNED_HEX PROC NEAR
    MOV AH, 9
    MOV DX, OFFSET HWX_MSG
    INT 21H

    CALL TO_UNSIGNED_HEX

    MOV AH, 9
    MOV DX, OFFSET HEX_STR
    INT 21H

    CALL MOVE_CARRIAGE
    CALL MOVE_CARRIAGE

    RET
OUTPUT_UNSIGNED_HEX ENDP

OUTPUT_SIGNED_BIN PROC NEAR
    MOV AH, 9
    MOV DX, OFFSET BIN_MSG
    INT 21H

    CALL TO_SIGNED_BIN

    MOV AH, 2
    MOV DL, SIGN
    INT 21H

    MOV AH, 9
    MOV DX, OFFSET BIN_STR
    INT 21H

    CALL MOVE_CARRIAGE
    CALL MOVE_CARRIAGE

    RET
OUTPUT_SIGNED_BIN ENDP

CODESEG ENDS
END